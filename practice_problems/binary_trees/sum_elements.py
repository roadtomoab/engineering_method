class TreeNode:
    def __init__(self, value = 0, left = None, right = None):
        self.value = value
        self.left = left
        self.right = right

def sum_tree(root: TreeNode) -> int:
    if not root:
        return 0
    
    return root.value + sum_tree(root.left) + sum_tree(root.right)

